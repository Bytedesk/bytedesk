package com.bytedesk.ticket.process;

import com.bytedesk.core.base.BaseEntity;
import com.bytedesk.core.constant.TypeConsts;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.Table;
import jakarta.persistence.Transient;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NoArgsConstructor;
import lombok.experimental.Accessors;
import lombok.experimental.SuperBuilder;

/**
 * 工单流程
 */
@Entity
@Data
@SuperBuilder
@Accessors(chain = true)
@EqualsAndHashCode(callSuper = true)
@AllArgsConstructor
@NoArgsConstructor
@Table(name = "bytedesk_ticket_process")
public class ProcessEntity extends BaseEntity {

    private static final long serialVersionUID = 1L;

    @Column(name = "process_name")
    private String name;

    @Column(name = "process_key")
    private String key;

    private String description;
    
    /**
     * Form structure definition stored as JSON format
     * Contains the complete form schema generated by frontend low-code editor
     */
    @Column(name = "process_schema", columnDefinition = TypeConsts.COLUMN_TYPE_TEXT)
    private String schema;

    /**
     * 
     */
    @Builder.Default
    @Column(name = "process_type")
    private String type = ProcessTypeEnum.TICKET_INTERNAL.name();

    /**
     * Current status of the process (DRAFT, PUBLISHED, DISABLED)
     */
    @Builder.Default
    @Column(name = "process_status")
    private String status = ProcessStatusEnum.DRAFT.name();

    // 部署id
    private String deploymentId;

    @Transient
    public boolean isDeployed() {
        return ProcessStatusEnum.DEPLOYED.name().equalsIgnoreCase(this.status);
    }
}
