package com.bytedesk.core.socket.mqtt.initializer.websocket;

import io.netty.channel.ChannelHandlerContext;
import io.netty.channel.SimpleChannelInboundHandler;
import io.netty.handler.codec.http.websocketx.ContinuationWebSocketFrame;

public class ContinuationWebSocketFrameHandler extends SimpleChannelInboundHandler<ContinuationWebSocketFrame> {

    @Override
    protected void channelRead0(final ChannelHandlerContext ctx, final ContinuationWebSocketFrame msg)
            throws Exception {
        ctx.fireChannelRead(msg.content().retain());
    }
}