package com.bytedesk.kanban.todo_list;

import java.util.List;
import java.util.ArrayList;

import com.bytedesk.core.base.BaseEntity;
import com.bytedesk.core.constant.I18Consts;
import com.bytedesk.core.enums.LevelEnum;
import com.bytedesk.core.enums.PlatformEnum;
import com.bytedesk.kanban.project.ProjectEntity;
import com.bytedesk.kanban.task.TaskEntity;

import jakarta.persistence.CascadeType;
import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.EntityListeners;
import jakarta.persistence.FetchType;
import jakarta.persistence.OneToMany;
import jakarta.persistence.Table;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NoArgsConstructor;
import lombok.experimental.Accessors;

@Entity
@Data
@Builder
@Accessors(chain = true)
@EqualsAndHashCode(callSuper = true)
@AllArgsConstructor
@NoArgsConstructor
@EntityListeners({TodoListEntityListener.class})
@Table(name = "bytedesk_plugin_kanban_todo_list")
public class TodoListEntity extends BaseEntity {

    private String name;

    @Builder.Default
    private String description = I18Consts.I18N_DESCRIPTION;

    @Builder.Default
    @Column(name = "todo_type", nullable = false)
    private String type = TodoListTypeEnum.CUSTOMER.name();

    @Builder.Default
    @Column(name = "todo_color", nullable = false)
    private String color = "red";

    @Builder.Default
    @Column(name = "todo_order", nullable = false)
    private int order = 0;

    // task list
    @Builder.Default
    @OneToMany(cascade = CascadeType.DETACH, fetch = FetchType.LAZY)
    private List<TaskEntity> tasks = new ArrayList<>();

    // 
    private String projectUid;

    @Builder.Default
    private String level = LevelEnum.ORGANIZATION.name();

    @Builder.Default
    private String platform = PlatformEnum.BYTEDESK.name();

    private String userUid;
}
